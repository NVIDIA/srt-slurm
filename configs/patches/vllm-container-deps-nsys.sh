# Runs the standard container deps, then installs nsight-systems-cli (nsys)
# from NVIDIA's devtools APT repo. Commands mirror ~/docker/setup_vllm_container.sh.

set -euo pipefail

bash /configs/patches/vllm-container-deps.sh

apt update
apt install -y --no-install-recommends gnupg
echo "deb http://developer.download.nvidia.com/devtools/repos/ubuntu$(source /etc/lsb-release; echo "$DISTRIB_RELEASE" | tr -d .)/$(dpkg --print-architecture) /" \
    | tee /etc/apt/sources.list.d/nvidia-devtools.list
apt-key adv --fetch-keys http://developer.download.nvidia.com/compute/cuda/repos/ubuntu1804/x86_64/7fa2af80.pub
apt update
apt install -y nsight-systems-cli
